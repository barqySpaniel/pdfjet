package com.pdfjet;

/**
 *  Used to specify the cap style of a line.
 *  See the Line class for more information.
 */
public enum CapStyle {
    /**
     * BUTT cap style.
     */
    BUTT,
    /**
     * ROUND cap style.
     */
    ROUND,
    /**
     * PROJECTING_SQUARE cap style.
     */
    PROJECTING_SQUARE;
}
