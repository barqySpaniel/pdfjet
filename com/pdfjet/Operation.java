/**
 *  Used to specify the 'stroke', 'close' and 'fill' operations.
 *
 */
public class Operation {
    /** Default Constructor */
    public Operation() {
    }

    /** Operation STROKE */
    public static char STROKE = 'S';
    /** Operation CLOSE */
    public static char CLOSE = 's';
    /** Operation FILL */
    public static char FILL = 'f';
}
